def test_synth_samples_in_sync() -> None:
    """The committed ``EXTENDED_synth_*.xml`` files match what
    ``tools/build_synth_samples.py`` produces — i.e. nobody hand-edited
    a generated fixture without updating the generator."""
    import sys

    sys.path.insert(0, str(Path(__file__).parent.parent))
    from tools.build_synth_samples import _BUILDERS

    for name, builder in _BUILDERS.items():
        committed = (_SAMPLES / name).read_bytes()
        assert committed == builder(), (
            f"{name} is out of sync with tools/build_synth_samples.py — "
            f"re-run `uv run python tools/build_synth_samples.py`"
        )
